#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	size;
	size_t	i;
	char	*join;

	size = (ft_strlen(s1) + ft_strlen(s2) + 1);
	join = (char *) malloc(size);
	if (!(s1 || s2 || join))
		return (NULL);
	i = 0;
	while (*s1)
		join[i++] = *s1++;
	while (*s2)
		join[i++] = *s2++;
	join[i] = '\0';
	return (join);
}
